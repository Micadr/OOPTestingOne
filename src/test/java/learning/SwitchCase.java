package learning;

public class SwitchCase {

    public static void main(String[] args) {

        String cat="gagsgsa";
        switch (cat) {
            case "Tom":
                System.out.println("Great Cat!");
                break;
            case "Lucy":
                System.out.println("Bad Cat!");
                break;
                default:
                System.out.println("Try a different Cat");



        }
    }
}
